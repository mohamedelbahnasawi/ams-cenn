#!/usr/bin/env python
"""Paper-ready LaTeX tables + significance for this work.

Reads the (DATASETS_ALL-filtered) results via aggregate.load_all_results and the efficiency
profiles, and writes to experiments/aggregated/tables/:
  main_results.tex       MSE & MAE, mean±std, bold-best / underline-second  (CeNN-Full + baselines)
  ablation_results.tex   CeNN variants, MSE with Δ% vs S0-StableBase
  efficiency_results.tex  params / MACs / compute_ms / peak-infer-mem (anchor datasetxhorizon)
  significance.json       Friedman + Nemenyi posthoc + mean ranks + critical difference (CD)
and (if matplotlib plotting available) the CD diagram via analysis.plotting.fig_cd_diagram.

Significance (RQ1): each (dataset, horizon) is a Friedman "block"; only COMPLETE
blocks (every compared method present) are used. CD = q_alpha * sqrt(k(k+1)/(6N)) with q_alpha from
the studentized-range distribution (scipy). Pairwise Nemenyi p-values via scikit-posthocs if present.

Run:  .venv/Scripts/python.exe experiments/make_tables.py --anchor-dataset ETTh1 --anchor-horizon 96
"""
import argparse
import glob
import json
import sys
from pathlib import Path

import numpy as np
import pandas as pd

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))
from experiments.config import (AGGREGATED_DIR, EFFICIENCY_DIR, CENN_VARIANTS, CENN_VARIANTS_MAIN,  # noqa: E402
                                CENN_MAIN_VARIANT, CENN_DISPLAY_NAME, BASELINES_ALL,
                                DATASETS_ALL, DATASETS_SMALL, DATASETS_MEDIUM, DATASETS_HEADLINE, DATASETS_LARGE)
from experiments.aggregate import load_all_results                                     # noqa: E402

TAB_DIR = AGGREGATED_DIR / "tables"
EFF_DIR = EFFICIENCY_DIR
S0 = "CeNN_S0-StableBase"


def _disp(model):
    if model == f"CeNN_{CENN_MAIN_VARIANT}":
        return CENN_DISPLAY_NAME
    if model == "CeNN_C1C2-Ensemble":          # the headline (AMS-CeNN) minus the linear skip
        return f"{CENN_DISPLAY_NAME} (no skip)"
    return model.removeprefix("CeNN_") if model.startswith("CeNN_") else model


def _seedagg(df, models):
    """seed-mean & std of MSE/MAE per (model,dataset,horizon), restricted to `models`."""
    d = df[df["model"].isin(models)]
    agg = d.groupby(["model", "dataset", "horizon"]).agg(
        mse_mean=("mse", "mean"), mse_std=("mse", "std"),
        mae_mean=("mae", "mean"), mae_std=("mae", "std"),
        n_seeds=("seed", "nunique"),
    ).reset_index()
    agg[["mse_std", "mae_std"]] = agg[["mse_std", "mae_std"]].fillna(0.0)
    return agg


# ---------------------------------------------------------------------------
# 2.1 Main results table -- MSE & MAE, mean±std, bold/underline
# ---------------------------------------------------------------------------
def main_table(df, models, datasets=None, out_name="main_results.tex",
               caption=None, label="tab:main_results"):
    if datasets is not None:
        df = df[df["dataset"].isin(datasets)]
    agg = _seedagg(df, models)
    if agg.empty:
        print(f"[main_table:{out_name}] no rows"); return
    present = [m for m in models if m in set(agg["model"])]
    # keep the dataset order we were given (not alphabetical), horizons ascending within
    order = datasets if datasets is not None else sorted(agg["dataset"].unique())
    rows = [(d, int(h)) for d in order
            for h in sorted(agg[agg.dataset == d]["horizon"].unique())]
    if caption is None:
        caption = (r"Multivariate long-horizon forecasting (input-512). Each cell is "
                   r"MSE / MAE as mean$_{\pm\text{std}}$ over seeds. \textbf{Bold}: best. "
                   r"\underline{Underline}: second best.")

    def cell(metric, ds, h):
        sub = agg[(agg.dataset == ds) & (agg.horizon == h)]
        return {r.model: (getattr(r, f"{metric}_mean"), getattr(r, f"{metric}_std"))
                for r in sub.itertuples()}

    lines = [r"% auto-generated by experiments/make_tables.py -- do not edit by hand",
             r"\begin{table*}[!t]", r"\centering",
             r"\caption{" + caption + "}",
             r"\label{" + label + "}",
             r"\resizebox{\textwidth}{!}{",
             r"\begin{tabular}{cc|" + "cc" * len(present) + "}", r"\toprule",
             "Dataset & H & " + " & ".join(rf"\multicolumn{{2}}{{c}}{{{_disp(m)}}}" for m in present) + r" \\",
             " & & " + " & ".join("MSE & MAE" for _ in present) + r" \\", r"\midrule"]

    prev = None
    for ds, h in rows:
        if prev is not None and ds != prev:
            lines.append(r"\midrule")
        prev = ds
        cmse, cmae = cell("mse", ds, h), cell("mae", ds, h)
        # rank for bold/underline per metric
        def marks(cm):
            vals = sorted((v[0], m) for m, v in cm.items())
            best = vals[0][1] if vals else None
            second = vals[1][1] if len(vals) > 1 else None
            return best, second
        bmse, smse = marks(cmse); bmae, smae = marks(cmae)

        def fmt(cm, m, best, second):
            if m not in cm:
                return "--"
            mean, std = cm[m]
            s = f"{mean:.3f}$_{{\\pm{std:.3f}}}$"
            if m == best:
                return rf"\textbf{{{s}}}"
            if m == second:
                return rf"\underline{{{s}}}"
            return s

        cells = []
        for m in present:
            cells.append(fmt(cmse, m, bmse, smse))
            cells.append(fmt(cmae, m, bmae, smae))
        lines.append(f"{ds} & {h} & " + " & ".join(cells) + r" \\")

    lines += [r"\bottomrule", r"\end{tabular}}", r"\end{table*}"]
    out = TAB_DIR / out_name
    out.write_text("\n".join(lines))
    print(f"[main_table:{out_name}] {len(present)} methods x {len(rows)} (dataset,horizon) -> {out}")


# ---------------------------------------------------------------------------
# 2.2 Ablation table -- CeNN variants, MSE with Δ% vs S0-StableBase
# ---------------------------------------------------------------------------
def ablation_table(df, variants, datasets=None):
    models = [f"CeNN_{v}" for v in variants]
    if datasets is not None:
        df = df[df["dataset"].isin(datasets)]
    agg = _seedagg(df, models)
    if agg.empty:
        print("[ablation_table] no rows"); return
    horizons = sorted(agg["horizon"].unique())
    ds_order = datasets if datasets is not None else sorted(agg["dataset"].unique())
    datasets = [d for d in ds_order if d in set(agg["dataset"])]
    # baseline (S0) seed-mean MSE per (dataset,horizon) for Δ%
    s0 = {(r.dataset, r.horizon): r.mse_mean for r in agg[agg.model == S0].itertuples()}

    lines = [r"% auto-generated by experiments/make_tables.py",
             r"\begin{table*}[!t]", r"\centering",
             r"\caption{CeNN ablation on the four ETT datasets (MSE, seed-mean). $\Delta\%$ is "
             r"relative to S0-StableBase per (dataset, horizon); negative = better than substrate. "
             r"The ETT datasets share one controlled scaler (minmax) across all variants; Weather "
             r"(validation-selected identity scaler) and the high-channel-count datasets are out of "
             r"scope for this single-scaler comparison and appear only in the main results "
             r"(Table~\ref{tab:main_results}). " + CENN_DISPLAY_NAME + r"~(no skip) is the headline "
             r"with its zero-init linear residual removed; the headline's K${=}2$ is accuracy-neutral "
             r"vs K${=}8$ on this architecture (mean $\Delta$MSE $<0.001$ over matched "
             r"seeds/horizons), so the K${=}8$ reference does not confound the skip ablation.}",
             r"\label{tab:ablation}", r"\resizebox{\textwidth}{!}{",
             r"\begin{tabular}{ll|" + "c" * (len(horizons)) + "}", r"\toprule",
             "Dataset & Variant & " + " & ".join(f"H{h}" for h in horizons) + r" \\", r"\midrule"]
    prev = None
    for ds in datasets:
        if prev is not None:
            lines.append(r"\midrule")
        prev = ds
        for m in models:
            disp = _disp(m)
            cells = []
            for h in horizons:
                sub = agg[(agg.model == m) & (agg.dataset == ds) & (agg.horizon == h)]
                if sub.empty:
                    cells.append("--"); continue
                mse = float(sub.mse_mean.iloc[0])
                base = s0.get((ds, h))
                if base and m != S0:
                    d = 100.0 * (mse - base) / base
                    cells.append(f"{mse:.3f} ({d:+.1f}\\%)")
                else:
                    cells.append(f"{mse:.3f}")
            lines.append(f"{ds} & {disp} & " + " & ".join(cells) + r" \\")
    lines += [r"\bottomrule", r"\end{tabular}}", r"\end{table*}"]
    out = TAB_DIR / "ablation_results.tex"
    out.write_text("\n".join(lines))
    print(f"[ablation_table] {len(models)} variants x {len(datasets)}ds x {len(horizons)}H -> {out}")


def ablation_buildup_table(df, datasets=("ETTh1", "ETTh2", "ETTm1", "ETTm2")):
    """Focused ablation: the BUILD-UP chain (S0 -> C1 -> C2 -> ensemble -> +skip),
    one row per variant, MSE averaged over the 4 ETT datasets (shared minmax scaler -> unconfounded),
    per horizon + Avg + Delta% vs the headline. Every variant before the skip sits at ~0.333, the
    skip alone reaches 0.303, showing the skip is the accuracy driver while the bounded gate / spectral
    cap add provable stability at no accuracy cost and multi-scale helps only at long horizons."""
    chain = [("S0-StableBase",          r"S$_0$: stable substrate"),
             ("C1-BoundedTau",          r"\quad + bounded-$\tau$ gate (C1)"),
             ("C2-MultiScaleEnsemble",  r"\quad + multi-scale (C2)"),
             ("C1C2-Ensemble",          r"\quad + C1\,+\,C2 ensemble (no skip)"),
             ("C1C2-Skip-K2",           r"\quad \textbf{+ linear skip $=$ " + CENN_DISPLAY_NAME + "}")]
    d = df[df["dataset"].isin(list(datasets))]
    agg = _seedagg(d, [f"CeNN_{v}" for v, _ in chain])
    horizons = sorted(int(h) for h in agg["horizon"].unique())

    def mean_over_ett(v, h):
        sub = agg[(agg.model == f"CeNN_{v}") & (agg.horizon == h)]
        return float(sub.mse_mean.mean()) if not sub.empty else None

    hl_avg = sum(mean_over_ett("C1C2-Skip-K2", h) for h in horizons) / len(horizons)
    lines = [r"% auto-generated by experiments/make_tables.py",
             r"\begin{table}[!t]", r"\centering",
             r"\caption{Ablation build-up of " + CENN_DISPLAY_NAME + r" on the four ETT datasets "
             r"(MSE, seed-mean over ETT; shared min--max scaler). Every variant up to the ensemble "
             r"sits near $0.333$; the zero-init \emph{linear skip} alone reaches $0.303$, so it is the "
             r"accuracy driver. The bounded-$\tau$ gate and spectral cap add a provable stability "
             r"guarantee at no accuracy cost (C1 $\approx$ S$_0$), and multi-scale helps only at the "
             r"longer horizons (H336/H720). $\Delta\%$ is relative to " + CENN_DISPLAY_NAME + r".}",
             r"\label{tab:ablation}",
             r"\begin{tabular}{l" + "c" * (len(horizons) + 2) + "}", r"\toprule",
             "Variant & " + " & ".join(f"H{h}" for h in horizons) + r" & Avg & $\Delta\%$ \\",
             r"\midrule"]
    for v, disp in chain:
        vals = [mean_over_ett(v, h) for h in horizons]
        avg = sum(x for x in vals if x is not None) / len(vals)
        dpct = 100.0 * (avg - hl_avg) / hl_avg
        is_hl = (v == "C1C2-Skip-K2")
        cellfmt = (lambda x: f"\\textbf{{{x:.3f}}}") if is_hl else (lambda x: f"{x:.3f}")
        avgs = f"\\textbf{{{avg:.3f}}}" if is_hl else f"{avg:.3f}"
        dstr = r"\textbf{0.0} " if is_hl else f"{dpct:+.1f}"
        lines.append(f"{disp} & " + " & ".join(cellfmt(x) for x in vals) + f" & {avgs} & {dstr} \\\\")
    lines += [r"\bottomrule", r"\end{tabular}", r"\end{table}"]
    out = TAB_DIR / "ablation_buildup.tex"
    out.write_text("\n".join(lines))
    print(f"[ablation_buildup] {len(chain)} variants x {len(datasets)} ETT x {len(horizons)}H -> {out}")


# ---------------------------------------------------------------------------
# 2.3 Efficiency table -- params / MACs / compute_ms / peak-infer-mem (anchor)
# ---------------------------------------------------------------------------
def efficiency_table(anchor_ds, anchor_h):
    recs = []
    for f in sorted(glob.glob(str(EFF_DIR / f"*__{anchor_ds}__H{anchor_h}.json"))):
        try:
            recs.append(json.loads(Path(f).read_text()))
        except Exception:
            continue
    if not recs:
        print(f"[efficiency_table] no efficiency JSONs for {anchor_ds} H{anchor_h}"); return
    recs.sort(key=lambda r: r.get("macs_fwd", float("inf")))
    lines = [r"% auto-generated by experiments/make_tables.py",
             r"\begin{table}[!t]", r"\centering",
             rf"\caption{{Efficiency at {anchor_ds}, H={anchor_h} (one full multivariate forecast). "
             r"MACs = matmul+conv only (FlopCounterMode); TimesNet's FFT, and softmax/LayerNorm, "
             r"are \emph{not} counted, so its MACs are a lower bound. compute\_ms = CUDA-kernel "
             r"wall-time, which includes per-branch kernel-launch overhead and so does not order "
             r"monotonically with MACs (e.g.\ a multi-branch ensemble and its single-branch substrate "
             r"share MACs but differ in launch count; massively-parallel convolutions run fast per MAC).}",
             r"\label{tab:efficiency}", r"\begin{tabular}{l|rrrr}", r"\toprule",
             r"Model & Params & MACs (M) & compute (ms) & infer mem (MB) \\", r"\midrule"]
    for r in recs:
        macs = r.get("macs_fwd"); cms = r.get("compute_ms"); mem = r.get("peak_infer_mb")
        macs_str = f"{macs/1e6:.2f}" if macs is not None else "--"
        lines.append(f"{_disp(r['model'])} & {int(r['params']):,} & "
                     f"{macs_str} & {cms if cms is not None else '--'} & "
                     f"{mem if mem is not None else '--'} \\\\")
    lines += [r"\bottomrule", r"\end{tabular}", r"\end{table}"]
    out = TAB_DIR / "efficiency_results.tex"
    out.write_text("\n".join(lines))
    print(f"[efficiency_table] {len(recs)} models @ {anchor_ds} H{anchor_h} -> {out}")


# ---------------------------------------------------------------------------
# 2.7 Significance -- Friedman + Nemenyi + mean ranks + critical difference
# ---------------------------------------------------------------------------
def compute_cd_stats(df, models, alpha=0.05):
    """Friedman test over (dataset,horizon) blocks (seed-mean MSE) + Nemenyi CD. Returns a dict.
    Only COMPLETE blocks (every method present) are used -- Friedman requires no missing cells."""
    from scipy.stats import friedmanchisquare, rankdata, studentized_range

    agg = df[df["model"].isin(models)].groupby(["model", "dataset", "horizon"])["mse"].mean()
    if agg.empty:
        return {"error": "no rows"}
    pivot = agg.reset_index().pivot_table(index=["dataset", "horizon"], columns="model", values="mse")
    present = [m for m in models if m in pivot.columns]
    pivot = pivot[present].dropna(axis=0, how="any")  # complete blocks only
    N, k = pivot.shape
    res = {"alpha": alpha, "methods": [_disp(m) for m in present],
           "n_complete_blocks": int(N), "k_methods": int(k)}
    if N < 2 or k < 3:
        res["note"] = f"insufficient for Friedman (need >=3 methods, >=2 complete blocks; got k={k}, N={N})"
        return res
    # Friedman across methods
    stat, p = friedmanchisquare(*[pivot[m].values for m in present])
    res["friedman_stat"], res["friedman_p"] = float(stat), float(p)
    # mean ranks (rank 1 = lowest MSE within each block)
    ranks = np.vstack([rankdata(row) for row in pivot.values])  # Nxk, smaller MSE -> smaller rank
    mean_ranks = ranks.mean(axis=0)
    res["mean_ranks"] = {_disp(m): float(r) for m, r in zip(present, mean_ranks)}
    # Nemenyi critical difference
    q = float(studentized_range.ppf(1 - alpha, k, np.inf)) / np.sqrt(2.0)
    cd = q * np.sqrt(k * (k + 1) / (6.0 * N))
    res["critical_difference"] = float(cd)
    # optional pairwise Nemenyi p-values
    try:
        import scikit_posthocs as sp
        long = pivot.reset_index().melt(id_vars=["dataset", "horizon"], var_name="model", value_name="mse")
        # the Friedman block is (dataset, horizon) — NOT dataset alone, or scikit-posthocs sees
        # duplicate block entries (one per horizon) and errors out. Build a composite block id.
        long["block"] = long["dataset"].astype(str) + "_H" + long["horizon"].astype(str)
        long["block_id"] = long["block"].astype("category").cat.codes
        pm = sp.posthoc_nemenyi_friedman(long, melted=True, block_col="block",
                                         block_id_col="block_id", group_col="model", y_col="mse")
        res["nemenyi_p"] = {a: {b: float(pm.loc[a, b]) for b in pm.columns} for a in pm.index}
    except Exception as e:
        res["nemenyi_note"] = f"pairwise skipped ({type(e).__name__}: {e}); install scikit-posthocs"
    return res


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--anchor-dataset", default="ETTh1")
    ap.add_argument("--anchor-horizon", type=int, default=96)
    args = ap.parse_args()
    TAB_DIR.mkdir(parents=True, exist_ok=True)

    df = load_all_results()
    if df.empty:
        print("No results. Nothing to tabulate."); return

    main_models = [f"CeNN_{CENN_MAIN_VARIANT}"] + BASELINES_ALL
    # ONE all-7 main table: the linear-skip fixed the high-V failure, so ECL/Traffic
    # are now competitive and fold back in -- no separate scoped/high-V split (that was an artifact
    # of the old broken-on-high-V model, and reporting only the flattering scope read as cherry-picking).
    main_table(df, main_models, datasets=DATASETS_ALL,
               out_name="main_results.tex",
               caption=(r"Multivariate long-horizon forecasting across the seven standard benchmarks "
                        r"(input-512). Each cell is MSE / MAE as mean$_{\pm\text{std}}$ over seeds. "
                        r"\textbf{Bold}: best, \underline{underline}: second. " + CENN_DISPLAY_NAME +
                        r" uses a validation-selected input scaler per dataset (identity on Weather, "
                        r"minmax elsewhere); all re-run baselines use identity throughout. "
                        r"`--' denotes a run not yet completed (Traffic H336/720)."),
               label="tab:main_results")
    # Ablation is scoped to the 4 ETT datasets: they share one controlled scaler (minmax) for all
    # CeNN variants, so component deltas are not confounded. Weather uses a validation-selected
    # identity scaler for the headline only (per-dataset), so mixing it into a leave-one-out vs the
    # headline would conflate scaler with architecture -> excluded from the ablation.
    # Focused build-up table (tab:ablation) = the main ablation exhibit: skip is the accuracy driver,
    # gate/cap are free stability, multi-scale is long-horizon. The full per-variant dump stays as an appendix.
    ablation_buildup_table(df, datasets=DATASETS_SMALL)
    ablation_table(df, CENN_VARIANTS_MAIN + [v for v in CENN_VARIANTS if v not in CENN_VARIANTS_MAIN],
                   datasets=DATASETS_SMALL)   # full dump -> ablation_results.tex (appendix only)
    efficiency_table(args.anchor_dataset, args.anchor_horizon)

    # ONE significance analysis over all 7 datasets (the high-V fix means no scoped/full split).
    # Friedman + Nemenyi on the complete (dataset,horizon) blocks.
    fig_dir = AGGREGATED_DIR / "figures"
    sig = compute_cd_stats(df, main_models)
    (TAB_DIR / "significance.json").write_text(json.dumps(sig, indent=2))
    if sig.get("note"):
        print(f"[significance] {sig['note']}")
    else:
        print(f"[significance] Friedman p={sig.get('friedman_p', float('nan')):.4g}, "
              f"N={sig.get('n_complete_blocks')} blocks, k={sig.get('k_methods')}")
    if "mean_ranks" in sig:
        try:
            from experiments.analysis import plotting
            note = f"7 datasets; N={sig.get('n_complete_blocks')} blocks, k={sig.get('k_methods')}"
            plotting.fig_cd_diagram(sig["mean_ranks"], sig["critical_difference"], fig_dir,
                                    name="fig_cd_diagram", note=note)
            print("[significance] wrote fig_cd_diagram")
        except Exception as e:
            print(f"[significance] CD diagram skipped: {type(e).__name__}: {e}")
    print(f"\nTables -> {TAB_DIR}")


if __name__ == "__main__":
    main()
